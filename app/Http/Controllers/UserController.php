    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'username' => 'required',
            'email' => 'required|unique:users',
            'password' => 'string|min:8|required_with:confirm_password|same:confirm_password',
            'confirm_password' => 'string|required|min:8',
            'name' => 'required|unique:company_profile',
            'company_email' => 'required|unique:company_profile',
        ]);

        $user = User::create(['name' => $request->username, 'email' => $request->email, 'password' => Hash::make($request->password) ]);
        if( $request->has('role') && $request->role == 'company' )
        {
            $user->assignRole('company');
            $user->profile()->create($request->only('name','company_email','city','state'));
            return redirect('/company/dashboard');
        }

    }

